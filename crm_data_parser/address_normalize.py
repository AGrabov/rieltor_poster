_TYPE_TOKEN_RE = re.compile(r"[А-Яа-яІіЇїЄєҐґ\-]+")

# Folding для ПОРІВНЯННЯ (не для відображення): звести RU/UA-специфічні літери.
_FOLD_MAP = str.maketrans(
    {
        "і": "и",
        "ї": "и",
        "ы": "и",
        "є": "е",
        "э": "е",
        "ё": "е",
        "ґ": "г",
        "ь": "",
        "ъ": "",
        "'": "",
        "’": "",
        "ʼ": "",
        "`": "",
    }
)


def _build_city_index() -> dict[str, str]:
    """folded(рос. назва) → укр. назва."""
    return {fold_cyrillic(ru): ua for ru, ua in _RU_UA_CITY_RAW.items()}


def fold_cyrillic(s: str) -> str:
    """Звести рядок до форми для порівняння кандидатів (не для відображення)."""
    return s.strip().lower().translate(_FOLD_MAP)


def normalize_city(city: str) -> str:
    """Привести місто до українського написання.

    Зрізає префікс ('м.', 'г.', 'город', 'місто'), шукає у словнику RU→UA;
    якщо не знайдено — повертає рядок без префікса (як є).
    """
    stripped = _CITY_PREFIX_RE.sub("", city).strip()
    return _CITY_INDEX.get(fold_cyrillic(stripped), stripped)


def strip_street_type(street: str) -> str:
    """Зрізати тип вулиці ('вул.'/'ул.', 'пров.'/'пер.', 'просп.' тощо)."""
    return _STREET_TYPE_RE.sub("", street).strip()


def street_type_canon(text: str) -> str:
    """Канонічний тип вулиці (UA) з рядка або ``''``, якщо типу немає.

    Шукає перший відомий тип-токен (UA або RU) у тексті й приводить до
    канонічної укр. форми ('ул'/'вул' → 'вул', 'пер'/'пров' → 'пров').
    Працює і на CRM-вулиці ('пер. Шевченка'), і на адресі реєстру
    ('м.Київ, вулиця Львівська, 19').
    """
    for token in _TYPE_TOKEN_RE.findall(text.lower()):
        canon = _TYPE_CANON.get(token)
        if canon:
            return canon
    return ""


