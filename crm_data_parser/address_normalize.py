"""Нормалізація адреси для пошуку: RU→UA місто, зрізання типу вулиці, folding.

CRM зберігає частини адреси непослідовно (інколи російською). Реєстр кадастру
(zem.center) шукає лише за українським написанням і не толерує тип вулиці в
запиті. Ці хелпери приводять адресу до форми, придатної для пошуку, і дають
``fold_cyrillic`` для алфавіто-толерантного ПОРІВНЯННЯ кандидатів.
"""

from __future__ import annotations

import re

# ── Міста: російська → українська ─────────────────────────────────────────
# Засіяно містами, де реально працюють. Легко доповнюється.
